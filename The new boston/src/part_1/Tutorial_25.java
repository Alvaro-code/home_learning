package part_1;

public class Tutorial_25 {

	public static void main(String[] args) {
		System.out.println(Math.abs(16));//Absolute value, how far away is it to 0
		System.out.println(Math.ceil(8.987));//Rounds up
		System.out.println(Math.floor(9.76));//Rounds down
		System.out.println(Math.max(8.9, 56.9));//The biggest of both values
		System.out.println(Math.min(3.74, -99));//The smallest of both values
		System.out.println(Math.pow(3, 3));//The power
		System.out.println(Math.sqrt(9));//Square root

	}

}
